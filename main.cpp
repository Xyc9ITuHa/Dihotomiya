    double result_bisection = dih->solve(testFunction); // AND AGAIN I HAVE TO CALL WITH THIS UGLY SYNTAX RHGAGGGHHGHHAAAA ╰（‵□′）╯
    std::cout << "Bisection method result: " << result_bisection << std::endl;

